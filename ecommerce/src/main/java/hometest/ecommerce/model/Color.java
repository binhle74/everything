package hometest.ecommerce.model;

/**
 * The Enum Color.
 */
public enum Color {
	
	/** The black. */
	BLACK("Black");
	
	/** The color name. */
	private String colorName;
	
	/**
	 * Instantiates a new color.
	 *
	 * @param colorName the color name
	 */
	Color(String colorName) {
		this.colorName = colorName;
	}
}
